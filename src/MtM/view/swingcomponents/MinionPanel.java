    public void addMinionBtn(Minion m) {
        MinionButton newBtn = new MinionButton(m);
        int size = getWidth();
        newBtn.setPreferredSize(new Dimension(size - 10, size / 2 - 20));
        newBtn.setBtnID(index);
        btnArray[index++] = newBtn;
        add(newBtn);
        newBtn.setMPanel(this);
    }

